# Example of a lambda function
square = lambda x: x ** 2

# Using the lambda function
result = square(5)
print("Square of 5:", result)

'''In Python, a lambda function is a small, anonymous function defined using the lambda keyword.
Lambda functions can take any number of arguments but can only have one expression.
In this example, square is a lambda function that takes one argument x and returns the square of x.
We can call the lambda function square with an argument, such as 5, and it will return the square of 5, which is 25.'''